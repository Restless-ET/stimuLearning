  <h2>The Problem</h2>
  <p>The player becomes a telecom operator, which is challenged to reach customers in
      a new region, facing some competition during the process. </p>
  <p>The operator is able to setup tariffs, to choose the network architecture that
      better suits his offer and to change his options along the simulation time.</p>
  <p>The main goal is to achieve the greater market share in the end of the
      simulation.</p>
