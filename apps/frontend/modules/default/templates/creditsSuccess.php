  <h3 style="text-align: left">
      Stimulating Learning in Engineering Students by Collaborative Entrepreneurship
      Training</h3>
  <p>This tool is part of project StimuLearning project, that aims to promote improved
      learning and entrepreneurial spirit among engineering students by resorting to
      the simulation of real world business cases in a collaborative mobile digital
      environment. </p>
  <p>This project implements project-based learning combined with an interactive
      entrepreneurial atmosphere where several industry professionals participate,
      helping students to create real world business cases, with different teams
      playing the roles of competing companies in a marketplace.</p>
  <p>Stimulearning is a project from Bandwidth Systems Group (GSBL) of Aveiro
      University. <a href="http://gsbl.det.ua.pt/gsbl_en/hp-stimulearning.aspx">More info...</a> </p>
